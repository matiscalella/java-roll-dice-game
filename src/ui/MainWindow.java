    public String getDiceAscii(int value) {

        String dice = "";

        String dice1 = """
        -------
        |     |
        |  o  |
        |     |
        -------
        """;

        String dice2 = """
        -------
        | o   |
        |     |
        |   o |
        -------
        """;

        String dice3 = """
        -------
        | o   |
        |  o  |
        |   o |
        -------
        """;

        String dice4 = """
        -------
        | o o |
        |     |
        | o o |
        -------
        """;

        String dice5 = """
        -------
        | o o |
        |  o  |
        | o o |
        -------
        """;

        String dice6 = """
        -------
        | o o |
        | o o |
        | o o |
        -------
        """;

        switch (value) {
            case 1 -> dice = dice1;
            case 2 -> dice = dice2;
            case 3 -> dice = dice3;
            case 4 -> dice = dice4;
            case 5 -> dice = dice5;
            case 6 -> dice = dice6;
            default -> dice = "";
        }
        return dice;
    }
    
    private void animateDiceRoll(Dice dice) {
        animationStep = 0;
        int maxSteps = 5; 

        diceTimer = new Timer(180, e -> {
            animationStep++;
            int tempRoll = (int)(Math.random() * 6) + 1;
            txtOutput.append(getDiceAscii(tempRoll) + "\n");
            
            if (animationStep >= maxSteps) {
                diceTimer.stop();
                finalRoll = dice.roll();
                txtOutput.append(getDiceAscii(finalRoll) + "\n");
                int roll = finalRoll;
                currentPlayer.addToScore(roll);
                txtOutput.append(currentPlayer.getName() + " rolled: [" + roll + "]\n");
                currentRollCount++;
                pauseTimer = new Timer(2000, ev -> {
                    pauseTimer.stop();

                    if (currentRollCount < maxRollsPerPlayer) {
                        animateDiceRoll(dice);
                    } else {
                        txtOutput.append("\nTotal score for " + currentPlayer.getName() + ": " + currentPlayer.getScore() + "\n");
                        if (currentPlayer == player1) {
                            currentPlayer = player2;
                            startTurn();
                        } else if (currentPlayer == player2) {
                            showFinalResult();
                        }
                    }
                });
                pauseTimer.start();
            }
        });
            diceTimer.start();
    }
    
    private void startTurn() {
        txtOutput.append("\n--- Turn: ["+ currentPlayer.getName() + "] ---\n");
        txtOutput.append("Rolling the dice for "+ currentPlayer.getName() +"...\n");
        currentRollCount = 0;
        animateDiceRoll(dice);
        
    }

    private void showFinalResult() {
        txtOutput.append("\n===== END OF GAME =====\n");
        txtOutput.append("FINAL SCORES:\n");
        txtOutput.append(player1.getName() + ": [" + player1.getScore() + "]\n");
        txtOutput.append(player2.getName() + ": [" + player2.getScore() + "]\n");

        if (player1.getScore() > player2.getScore()) {
            txtOutput.append("\nüèÜ Winner: " + player1.getName() + "\n");
        } else if (player2.getScore() > player1.getScore()) {
            txtOutput.append("\nüèÜ Winner: " + player2.getName() + "\n");
        } else {
            txtOutput.append("\nIt's a tie!\n");
        }
        txtOutput.append("=======================\n");

        currentRollCount = 0;
        currentPlayer = null;
    }
